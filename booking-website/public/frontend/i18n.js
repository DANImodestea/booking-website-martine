window.t = function(key) {
    return window.translations[window.currentLang]?.[key] || key;
};

window.applyTranslations = function() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (window.translations[window.currentLang][key]) {
            el.innerHTML = window.translations[window.currentLang][key];
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (window.translations[window.currentLang][key]) {
            el.placeholder = window.translations[window.currentLang][key];
        }
    });
    window.dispatchEvent(new Event('languageChanged'));
};

document.addEventListener("DOMContentLoaded", () => {
    window.applyTranslations();

    const updateLangUI = () => {
        document.querySelectorAll('.lang-btn-toggle').forEach(btn => {
            if (btn.dataset.lang === window.currentLang) {
                btn.classList.add('btn-secondary', 'text-white');
                btn.classList.remove('btn-outline-secondary');
            } else {
                btn.classList.add('btn-outline-secondary');
                btn.classList.remove('btn-secondary', 'text-white');
            }
        });
    };
    
    updateLangUI();

    document.querySelectorAll('.lang-btn-toggle').forEach(opt => {
        opt.addEventListener('click', (e) => {
            const lang = e.currentTarget.getAttribute('data-lang');
            localStorage.setItem('lang', lang);
            window.currentLang = lang;
            updateLangUI();
            window.applyTranslations();
        });
    });
});
