window.t = function(key) {
    return window.translations[window.currentLang]?.[key] || key;
};

window.applyTranslations = function() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (window.translations[window.currentLang][key]) {
            el.innerHTML = window.translations[window.currentLang][key];
        }
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (window.translations[window.currentLang][key]) {
            el.placeholder = window.translations[window.currentLang][key];
        }
    });
    window.dispatchEvent(new Event('languageChanged'));
};

document.addEventListener("DOMContentLoaded", () => {
    window.applyTranslations();

    const langBtn = document.getElementById('lang-btn');
    const langMenu = document.getElementById('lang-menu');
    
    if (langBtn && langMenu) {
        const flags = { fr: '🇫🇷', en: '🇬🇧' };
        langBtn.innerHTML = flags[window.currentLang];

        langBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            langMenu.style.display = langMenu.style.display === 'block' ? 'none' : 'block';
        });
        document.addEventListener('click', () => langMenu.style.display = 'none');

        document.querySelectorAll('.lang-option').forEach(opt => {
            opt.addEventListener('click', (e) => {
                const lang = e.currentTarget.getAttribute('data-lang');
                localStorage.setItem('lang', lang);
                window.currentLang = lang;
                langBtn.innerHTML = flags[lang];
                langMenu.style.display = 'none';
                window.applyTranslations();
            });
        });
    }
});